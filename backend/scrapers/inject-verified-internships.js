// MAIN: Inject into internships.json
// ═══════════════════════════════════════════════════════════════════
async function main() {
  const dataPath = join(__dirname, '..', 'data', 'scraped', 'internships.json');

  let data;
  try {
    const raw = await fs.readFile(dataPath, 'utf8');
    data = JSON.parse(raw);
  } catch (err) {
    console.error('Failed to read internships.json:', err.message);
    process.exit(1);
  }

  console.log(`\nLoaded existing data: ${data.internships?.length || 0} internships`);

  // Combine all entries: new verified + existing. The data-integrity module
  // handles dedup (verified always wins) and schema validation automatically.
  const { validateAndDedup } = await import('../services/data-integrity.js');
  const allEntries = [...VERIFIED_PROGRAMS, ...(data.internships || [])];
  const { clean, removed, warnings, stats } = validateAndDedup('internships', allEntries);

  console.log(`\n📊 Data Integrity Report:`);
  console.log(`  Input: ${stats.input} | Output: ${stats.output}`);
  console.log(`  Duplicates removed: ${stats.duplicatesRemoved}`);
  console.log(`  Invalid removed: ${stats.invalidRemoved}`);
  console.log(`  Verified: ${stats.verified} | Non-verified: ${stats.nonVerified}`);
  if (warnings.length > 0) {
    console.log(`\n⚠️  Warnings (${warnings.length}):`);
    warnings.slice(0, 10).forEach(w => console.log(`  ${w}`));
    if (warnings.length > 10) console.log(`  ... and ${warnings.length - 10} more`);
  }

  data.internships = clean;
  data.metadata.totalCount = clean.length;
  data.metadata.totalInternships = clean.length;
  data.metadata.verifiedCount = clean.filter(i => i._verified).length;
  data.metadata.lastScraped = new Date().toISOString().slice(0, 10);

  // Update sources
  if (!data.metadata.sources.includes('NSF REU Programs')) {
    data.metadata.sources.push(
      'NSF REU Programs',
      'NASA Internship Programs',
      'NIH Summer Internship Program',
      'Smithsonian Institution',
      'DOE National Laboratories (SULI)',
      'Seattle Children\'s Research Institute',
      'Fred Hutchinson Cancer Center',
      'University research programs (UW, UMich, Stanford, etc.)',
      'Hospital volunteer programs',
      'Environmental/conservation nonprofits'
    );
  }

  await fs.writeFile(dataPath, JSON.stringify(data, null, 2));

  const verifiedCount = clean.filter(i => i._verified).length;
  const byState = {};
  for (const i of clean) {
    const st = i.location?.state || 'Other';
    byState[st] = (byState[st] || 0) + 1;
  }

  console.log(`\n✅ Injected ${verifiedCount} verified programs`);
  console.log(`Total internships: ${clean.length}`);
  console.log(`\nBy state (top 10):`);
  const sorted = Object.entries(byState).sort((a, b) => b[1] - a[1]).slice(0, 10);
  for (const [state, count] of sorted) {
    console.log(`  ${state}: ${count}`);
  }

  // Count unpaid
  const unpaidCount = clean.filter(i => !i.paid).length;
  const unpaidWA = clean.filter(i => !i.paid && i.location?.state === 'WA').length;
  console.log(`\nUnpaid total: ${unpaidCount}`);
  console.log(`Unpaid WA: ${unpaidWA}`);
}

main().catch(console.error);
